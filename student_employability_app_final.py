import streamlit as st
import numpy as np
import joblib

# Load model & scaler
model = joblib.load("employability_predictor.pkl")
scaler = joblib.load("scaler.pkl")

st.set_page_config(page_title="üéì Employability Predictor", layout="centered")
st.title("üéì Student Employability Predictor")
st.write("Predict whether a student is **Employable** or **Less Employable** based on their profile.")

# üî∑ Replace with actual feature names
feature_names = [
    "Feature_1", "Feature_2", "Feature_3", "Feature_4", "Feature_5"
]

# User inputs
inputs = []
for feature in feature_names:
    val = st.number_input(f"Enter {feature}:", value=0.0)
    inputs.append(val)

if st.button("Predict"):
    X = np.array(inputs).reshape(1, -1)
    X_scaled = scaler.transform(X)
    pred = model.predict(X_scaled)[0]
    prob = model.predict_proba(X_scaled)[0][pred]

    if pred == 1:
        st.success(f"‚úÖ Employable! (Confidence: {prob:.2f})")
    else:
        st.warning(f"‚ö†Ô∏è Less Employable (Confidence: {prob:.2f})")


